#!/usr/bin/env python3

# Prompt the user to input their current age
current_age = int(input("How old are you? "))

# Calculate the age in 2050
current_year = 2023
future_year = 2050
age_in_2050 = current_age + (future_year - current_year)

# Print the result
print(f"In 2050, you will be {age_in_2050} years old.")
